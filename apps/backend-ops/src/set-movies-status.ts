import { runChunks } from "@blockframes/firebase-utils";
import { createAppConfig, Movie } from "@blockframes/model";

export async function setMoviesStatus(db: FirebaseFirestore.Firestore) {

  const movies = await db.collection('movies').get();

  return runChunks(movies.docs, async (doc) => {
    const movie = doc.data() as Movie;
    const now = new Date();
    let update = false;

    if (movieIds.includes(movie.id)) {
      movie.app.festival = createAppConfig({
        access: true,
        status: 'accepted',
        acceptedAt: now
      })

      update = true;
    }

    if (update) await doc.ref.set(movie);
  }).catch(err => console.error(err));
}

